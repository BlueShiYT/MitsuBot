let handler = async (m, { conn, text }) => {
  if (!text) throw 'Tidak ada teks'
  m.reply(text, false, {
    contextInfo: {
      mentionedJid: conn.parseMention(text)
    }
  })
}
handler.help = ['mention <teks>']
handler.tags = ['tools']
handler.register = true

handler.command = /^mention$/i

module.exports = handler
